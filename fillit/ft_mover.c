#include "fillit.h"

void	ft_mover(t_pos *cp, int bul)
{
	int i;

	i = 1;
	if (bul == 0)
	{
		i = 1;
		while (i)
			i = ft_x_left(cp, 1);
	}
	else
	{
		while (i)
			i = ft_y_up(cp, 1);
	}
}
