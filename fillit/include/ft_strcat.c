char	*ft_strcat(char *s1, const char *s2)
{
	char		*ps1;

	ps1 = s1;
	while (*ps1 != '\0')
		ps1++;
	while (*s2 != '\0')
	{
		*ps1 = *s2;
		s2++;
		ps1++;
	}
	*ps1 = '\0';
	return (s1);
}
