#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char		*ps1;

	ps1 = s1;
	while (*ps1 != '\0')
		ps1++;
	while (*s2 != '\0' && n)
	{
		*ps1 = *s2;
		ps1++;
		s2++;
		n--;
	}
	*ps1 = '\0';
	return (s1);
}
