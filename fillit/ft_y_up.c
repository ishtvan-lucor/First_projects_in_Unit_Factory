#include "fillit.h"

int	ft_y_up(t_pos *pos, int k)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if ((pos->y[i] - 1) < 0)
			return (0);
		i++;
	}
	i = 0;
	while (i < 4)
	{
		pos->y[i] -= k;
		i++;
	}
	return (1);
}
